package com.app.jiwon.tekken7_manual.Items;

public class FrameRecyclerViewItem {
    private String skillName;
    private String command;
    private String hitLevel;
    private String damage;
    private String startFrame;
    private String blockFrame;
    private String hitFrame;

    public String getSkillName() {
        return skillName;
    }

    public void setSkillName(String skillName) {
        this.skillName = skillName;
    }

    public String getCommand() {
        return command;
    }

    public void setCommand(String command) {
        this.command = command;
    }

    public String getHitLevel() {
        return hitLevel;
    }

    public void setHitLevel(String hitLevel) {
        this.hitLevel = hitLevel;
    }

    public String getDamage() {
        return damage;
    }

    public void setDamage(String damage) {
        this.damage = damage;
    }

    public String getStartFrame() {
        return startFrame;
    }

    public void setStartFrame(String startFrame) {
        this.startFrame = startFrame;
    }

    public String getBlockFrame() {
        return blockFrame;
    }

    public void setBlockFrame(String blockFrame) {
        this.blockFrame = blockFrame;
    }

    public String getHitFrame() {
        return hitFrame;
    }

    public void setHitFrame(String hitFrame) {
        this.hitFrame = hitFrame;
    }

    public String getCounter_frame() {
        return counter_frame;
    }

    public void setCounter_frame(String counter_frame) {
        this.counter_frame = counter_frame;
    }

    public String getNote() {
        return note;
    }

    public void setNote(String note) {
        this.note = note;
    }

    private String counter_frame;
    private String note;
}
